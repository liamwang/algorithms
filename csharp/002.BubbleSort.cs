namespace Algorithms
{
    /// <summary>
    /// 冒泡排序 O(n^2)
    /// </summary>
    public static class BubbleSort
    {
        public static void Execute(int[] arr)
        {
            for (int i = 0; i < arr.Length - 1; i++)
            {
                for (int j = 0; j < arr.Length - 1 - i; j++)
                {
                    if (arr[j] > arr[j + 1])
                    {
                        int temp = arr[j];
                        arr[j] = arr[j + 1];
                        arr[j + 1] = temp;
                    }
                }
            }
        }
    }
}
